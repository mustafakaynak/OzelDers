package ders1Giris;

public class AtamaOperatrorleri {

	public static void main(String[] args) {
		
		int sayi = 10;
		
		sayi = sayi + 10;
		sayi += 10;
		
		sayi = sayi - 10;
		sayi -= 10;
		
		sayi = sayi * 10;
		sayi *= 10;
		
		
		sayi = sayi / 10;
		sayi /= 10;
		
		sayi = sayi % 10;
		sayi %= 10;
		

	}

}
