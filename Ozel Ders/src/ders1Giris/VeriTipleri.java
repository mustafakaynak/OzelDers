package ders1Giris;

public class VeriTipleri {

	public static void main(String[] args) {
		
		/*
		 * VERİ TİPLERİ
		 * Tamsayı: varsayılan int
		 * byte		: 1 byte (8 bit: 11110000)
		 * short	: 2 byte
		 * int		: 4 byte
		 * long		: 8 byte
		 * 
		 * Ondalıklı Sayı: varsayılan double
		 * float	: 4 byte
		 * double	: 8 byte
		 * 
		 * Karakter:
		 * char		: 2 byte
		 * 
		 * Mantık:
		 * boolean	: 1 bit
		 * 
		*/
		
		byte b = 127;
		short s = 32767;
		int i = 2147483647;
		long l = 2147483648l;
		
		float f = 10.208392f;
		double d = 10.67468736438;
		
		char c = 'c';
		
		boolean m = true;		
		
	}

}
